// for loop

#include <iostream>

using namespace std;

int main(){


    // for loop 

    for(int i =1; i <=5; i++ ){

        cout << i << endl;
    }

    // while loop

    int a = 10;
    while(a < 20){
        cout << a<< endl;
        ++a;

    }


    // do while loop

    int b = 10;

    do{
        cout << b;

        ++b;
    }
    while (b < 20);

    // break and continue


}